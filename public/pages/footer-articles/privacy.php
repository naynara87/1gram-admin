            <h3>7. 개인정보 자동 수집 장치의 설치•운영 및 거부에 관한 사항</h3>
            <p>1gram은 개별적인 맞춤서비스를 제공하기 위해 이용정보를 저장하고 수시로 불러오는 ‘쿠기(cookie)’를 사용합니다.<br>
                쿠키는 웹사이트를 운영하는데 이용되는 서버가 이용자의 컴퓨터 브라우저에게 보내는 소량의 정보이며 이용자들의 PC 컴퓨터내의 하드디스크에 저장되기도 합니다.</p>
            <ul>
                <li>가. 쿠키의 사용 목적 :
                    <ul>
                        <li>이용자가 방문한 각 서비스와 웹 사이트들에 대한 방문 및 이용형태, 인기 검색어, 보안접속 여부, 등을 파악하여 이용자에게 최적화된 정보 제공을 위해
                            사용됩니다.</li>
                    </ul>
                </li>
                <li>나. 쿠키의 설치∙운영 및 거부 :
                    <ul>
                        <li>이용자는 사용하는 웹 브라우저의 옵션을 설정함으로써 모든 쿠키를 허용하거나 쿠키를 저장할 때마다 확인을 거치거나, 모든 쿠키의 저장을 거부할 수
                            있습니다.<br>단, 쿠키 저장을 거부할 경우 맞춤형 서비스 이용에 어려움이 발생할 수 있습니다.</li>
                    </ul>
                </li>
                <li>다. 설정 방법의 예
                    <ul>
                        <li>1) Google Chrome : 웹 브라우저 우측 메뉴 &gt; [설정] &gt; [개인정보 및 보안] &gt; 쿠키</li>
